class Calc:

  def add(self, number1, number2):
    return float(number1) + float(number2)

  def sub(self, number1, number2):
    return float(number1) - float(number2)

  def mul(self, number1, number2):
    return float(number1) * float(number2)

  def div(self, number1, number2):
    return float(number1) / float(number2)

  def mod(self, number1, number2):
    return float(number1) % float(number2)

